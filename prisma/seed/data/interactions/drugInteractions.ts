/**
 * Helper function to get all interactions for a specific drug
 */
export function getInteractionsForDrug(drugId: string): DrugInteractionSeedData[] {
  return drugInteractions.filter(
    interaction => interaction.drugAId === drugId || interaction.drugBId === drugId
  );
}

/**
 * Helper function to get interactions between two specific drugs
 */
export function getInteractionBetween(drugAId: string, drugBId: string): DrugInteractionSeedData | undefined {
  return drugInteractions.find(
    interaction => 
      (interaction.drugAId === drugAId && interaction.drugBId === drugBId) ||
      (interaction.drugAId === drugBId && interaction.drugBId === drugAId)
  );
}